    public function findAnswer(string $query): ?string
    {
        $normalized = trim(mb_strtolower($query));
        foreach ($this->faq as $q => $a) {
            if ($normalized === mb_strtolower($q)) {
                return $a;
            }
        }
        return null;
    }
}